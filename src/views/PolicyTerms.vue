    제 22 조 (할인쿠폰)
    <ol>
      <li>“할인쿠폰”은 회사가 무상으로 발행하는 쿠폰으로, 발행대상, 발행경로, 사용대상 등에 따라 구분될 수 있으며, “할인쿠폰”의 세부구분, 할인금액(할인율), 사용방법, 사용기한 및 제한에 대한 사항은 “할인쿠폰” 또는 서비스화면에 표시됩니다. “할인쿠폰”의 종류 및 내용과 발급여부에 관하여는 회사의 각 서비스에 대한 정책에 따라 달라질 수 있습니다.</li>
      <li>“할인쿠폰”은 현금으로 출금될 수 없으며, “할인쿠폰”에 표시된 유효기간이 만료되거나 이용계약이 종료되면 소멸합니다.</li>
      <li>“할인쿠폰”은 회사에서 별도로 명시한 경우를 제외하고는 타인에게 양도할 수 없으며, 부정한 목적이나 용도로 사용할 수 없습니다. 이를 위반한 경우 회사는 “할인쿠폰”을 소멸시키거나 회원자격을 정지할 수 있습니다.</li>
      <li>“회원”이 부정한 방법으로 “포인트”, “할인쿠폰”을 획득한 사실이 확인될 경우 “회사”는 “회원”의 “포인트”, “할인쿠폰” 회수, “아이디” 삭제 및 형사고발 등 기타 조치를 취할 수 있습니다.</li>
    </ol>
