    제 24 조 (서비스 권리의 귀속 및 게시물의 이용)
    <ol>
      <li>“유료서비스”로 인해 “회원”에게 제공된 결과물에 대한 저작권 등 지적재산권은 “회원”에게 귀속되며, “회사”는 이 정보를 보호할 의무가 있습니다.</li>
      <li>“회원”이 “서비스” 내에 등록하는 “게시물”은 “회사”가 개발ㆍ제공하는 통계 및 별도의 부가 서비스 등에 “게시물”의 저작자를 식별할 수 없는 통계 형태로 활용될 수 있으며, 이 과정에서 해당 “게시물”은 일부 수정,복제 및 편집될 수 있습니다. 이 경우, 회사는 저작권법 규정을 준수하며, “회원”은 언제든지 고객센터를 통해 해당 “게시물”에 대해 삭제, 부가서비스에 대한 사용 제외, 비공개 등의 조치를 취할 수 있습니다.</li>
      <li>“회사”는 제2항 이외의 방법으로 “회원”의 “게시물”을 이용하고자 하는 경우에는 전화, 팩스, 전자우편 등을 통해 사전에 “회원”의 동의를 얻어야 합니다.</li>
      <li>“회원”의 “게시물”이 “정보통신망법” 및 “저작권법”등 관련법에 위반되는 내용을 포함하는 경우, 권리자는 관련법이 정한 절차에 따라 해당 “게시물”의 게시중단 및 삭제 등을 요청할 수 있으며, “회사”는 관련법에 따라 조치를 취하여야 합니다.</li>
      <li>“회사”는 전항에 따른 권리자의 요청이 없는 경우라도 권리침해가 인정될 만한 사유가 있거나 기타 회사 정책 및 관련법에 위반되는 경우에는 관련법에 따라 해당 “게시물”에 대해 임시조치 등을 취할 수 있습니다.</li>
      <li>“서비스”에 대한 저작권 및 지적재산권은 “회사”에 귀속됩니다. 단, “회원”의 “게시물” 및 제휴계약에 따라 제공된 저작물 등은 제외합니다.</li>
      <li>“회사”는 서비스와 관련하여 “회원”에게 “회사”가 정한 이용조건에 따라 “아이디”, 콘텐츠, “포인트” 등을 이용할 수 있는 이용권한을 부여하며, “회원”은 이를 양도, 판매, 담보제공 등의 처분행위를 할 수 없습니다.</li>
    </ol>
