    제 23 조 (포인트)
    <ol>
      <li>“포인트”는 “회사”가 운영하는 사이버 화폐로서 사이트에서 결제 수단으로 사용할 수 있습니다.</li>
      <li>“포인트”는 전자금융거래법령에 따라 그 합계가 최대 200만 원을 초과하여 충전 및 적립되지 아니하고, 전자금융거래법 등 관련 법령에 따라 그 보유한도가 달라질 수 있습니다.</li>
      <li>유상으로 발행된 “포인트”의 유효 기간은 그 충전일 혹은 지급일로부터 5년입니다.</li>
      <li>무상으로 제공되는 “포인트”의 유효기간은 그 적립일로부터 6개월입니다. 단, 무상의 “포인트”를 사용하여 제품 등을 구매하였다가 구매취소로 인하여 재 지급 되는 무상의 “포인트” 유효기간은 재 지급 시점을 기준으로 6개월이고, 기타 마케팅 및 프로모션을 통하여 지급되는 무상의 “포인트”의 유효기간은 각각 별도로 설정될 수 있습니다.</li>
      <li>무상의 “포인트”의 유효기간은 월별로 관리되고 지급된 월로부터 6개월이 지난 후 해당 월의 말일에 자동적으로 소멸됩니다. “회사”는 이벤트 등을 통해 무상으로 적립된 “포인트”의 적립 사유가 취소되는 경우에는 이를 환수할 수 있습니다.</li>
      <li>“회원”은 무상의 “포인트”를 그 사용 기간 이내에 사용하여야 하고, 유효 기간 이내에 사용하지 않을 경우 소멸됩니다.</li>
      <li>“포인트”는 유효기간이 빠른 순서로 사용되며, “포인트”의 경우 유효기간이 만료되거나 이용계약이 종료되면 미 사용한 “포인트”는 소멸 합니다.</li>
      <li>“회원”은 보유 중인 “포인트”의 환불을 “회사”에 요구할 수 있으나, 환불의 대상이 되는 “포인트”는 회원이 유상으로 충전하여 보유하고 있는 것에 한정되며, 무상으로 제공받은 것은 포함되지 않습니다.</li>
      <li>“회사”가 “회원”과 별도의 계약을 통해 지급한 “포인트”에 대해서 해당 계약의 환불 조항을 준수합니다.</li>
      <li>“회사”는 “회원”으로부터 환불처리에 필요한 서류를 수령하고 “회원”이 충전을 위하여 입금을 완료한 사실을 확인한 후, 7 영업일 이내에 “회원”이 지정한 계좌로 해당 환불금액을 입금합니다. 단, 결제 수단에 따라 입금확인에 일정기간이 추가로 소요되는 경우 환불처리가 지연 될 수 있습니다.</li>
      <li>“포인트”의 환불 금액이 1000원 미만일 경우, 환불이 제한될 수 있습니다.</li>
      <li>“포인트”의 환불에 소요되는 비용은 “회원”의 귀책사유로 인한 환불의 경우에는 “회원”이, “회사”의 귀책사유로 인한 환불의 경우에는 “회사”가 각각 부담합니다.</li>
    </ol>
